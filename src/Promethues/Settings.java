   public static class ColorManager {
        private static String selectedColor = "Green";
        private static Preferences prefs = Preferences.userNodeForPackage(Settings.class);
        public static void setPanelColor(JPanel panel, String colorName) {
            switch (colorName) {
                
                case "Green":
                    panel.setBackground(new Color(51, 153, 0)); // Green
                    break;
                case "Blue":
                    panel.setBackground(new Color(0, 102, 204)); // Blue
                    break;
                case "Black":
                    panel.setBackground(Color.BLACK); // Black
                    break;
                case "Orange":
                    panel.setBackground(new Color(255, 165, 0)); // Orange
                    break;
                default:
                    panel.setBackground(Color.WHITE); // Default color
                    break;
            
        }
          selectedColor = colorName;      
            prefs.put("selectedColor", selectedColor);      
         
    }
  
         public static String getSelectedColor() {
            return prefs.get("selectedColor", selectedColor); }
}
   
