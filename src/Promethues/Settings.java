private void fetchData() {
        String url = "jdbc:mysql://localhost/prometheus";
        String user = "root";
        String password = ""; // No password

        try (Connection conn = DriverManager.getConnection(url, user, password)) {
            String sql = "SELECT date, cashier, subtotal, pay, balance FROM sales";
            PreparedStatement statement = conn.prepareStatement(sql);
            ResultSet resultSet = statement.executeQuery();

            // Get the JTable model
            DefaultTableModel model = (DefaultTableModel) reportt.getModel();

            // Add column names to the model
            model.setColumnIdentifiers(new String[]{"Date", "Cashier", "Subtotal", "Pay", "Balance"});

            // Add rows to the model
            while (resultSet.next()) {
                Object[] row = {
                    resultSet.getDate("date"),
                    resultSet.getString("cashier"),
                    resultSet.getDouble("subtotal"),
                    resultSet.getDouble("balance"),
                    resultSet.getDouble("pay")
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
